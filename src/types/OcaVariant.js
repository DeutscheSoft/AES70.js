export class OcaVariant {
  /**
   * A class holding the value of an OcaVariant.
   * @class OcaVariant
   */
  constructor(index, value) {
    /**
     * The index of the variant alternative.
     * @type number
     */
    this.Index = index;
    /**
     * The value.
     * @type any
     */
    this.Value = value;
  }
}
